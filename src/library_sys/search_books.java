    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        
       int row = this.jTable1.getSelectedRow();
       this.jTextField2.setText(dtm.getValueAt(row, 0).toString());
       this.jTextField3.setText(dtm.getValueAt(row, 1).toString());
       this.jTextField4.setText(dtm.getValueAt(row, 2).toString());
       this.jTextField5.setText(dtm.getValueAt(row, 3).toString());
       this.jTextField6.setText(dtm.getValueAt(row, 4).toString());
       this.jTextField7.setText(dtm.getValueAt(row, 5).toString());
       
       if(jTextField7.getText().equals("0")){
            jLabel9.setForeground(Color.RED);
       jLabel9.setText("Book Is Not Available");
       }else{
           jLabel9.setForeground(Color.GREEN);
           jLabel9.setText("Book Is Available");
       
       }
      
        
    }//GEN-LAST:event_jTable1MouseClicked

