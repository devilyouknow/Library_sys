    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        try {
            ResultSet rs = DB.getData("select* from books where bid='"+jTextField1.getText()+"' or ISBN='"+jTextField1.getText()+"' or bname='"+jTextField1.getText()+"'");
            while (rs.next()){
                Vector v = new Vector();
                v.add(rs.getString("bid"));
                v.add(rs.getString("ISBN"));
                v.add(rs.getString("bname"));
                v.add(rs.getString("bauthor"));
                v.add(rs.getString("bprice"));
                v.add(rs.getString("bavailable"));
                
                
                
                
                dtm.addRow(v);
                
            }
            
        } catch (Exception e) {
            e.printStackTrace();
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

